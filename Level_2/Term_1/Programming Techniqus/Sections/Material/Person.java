package javaapplication2;

public class Person {
  private String name;
  private int id;
  // Getter
  public String getName() {
    return name;
  }

  // Setter
  public void setName(String newName) {
    this.name = newName;
  }
  
  public int getId() {
    return id;
  }

  public void setId(int newId) {
    this.id = newId;
  }
}
